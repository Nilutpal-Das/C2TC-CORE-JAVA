package programs;

public class Array_Example2 {

	public static void main(String[] args) {
		int[] arr = new int[5];
		arr[0] = 20;
		arr[1] = 30;
		arr[2] = 40;
		arr[3] = 50;
		arr[4] = 90;
		
		for(int i=0; i<arr.length; i++) {
			
			System.out.print(arr[i] + "   ");
			
		}
		System.out.println();

	}
   }


